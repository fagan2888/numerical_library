function [f,c,g,J,H,G,Hc] = p3_2_fun(x,y)

    if nargin < 2
        y = [0;0;0];
    end

    g = [ 18;
         -10;
          18;
         -12;
          -6];

    H = [ 4  3 -4  0 -1;
          3 -4 -2 -4 -1;
         -4 -2 -4  0  2;
          0 -4  0 -4 -1;
         -1 -1  2 -1  4];
    
   
    g = g + H*x;

    c = [ -3;
          18;
          21];

    J = [ 5  0  1  3  3;
         -7  0 -7  4  4;
         -3  7  8  5  0];

    c = J*x+c;

    f = g'*x + (1/2)*x'*H*x;

    G = [g-J'*y;
         -c    ];

    Hc = zeros(5,5,3);

end
 
